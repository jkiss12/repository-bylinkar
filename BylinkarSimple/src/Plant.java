
public class Plant {
	private String svkName;
	private String latName;
	private String page;
	
	public Plant(String s,String l, String p) {
		svkName = s;
		latName = l;
		page = p;
	}

	/**
	 * @return the svkName
	 */
	public String getSvkName() {
		return svkName;
	}

	/**
	 * @return the latName
	 */
	public String getLatName() {
		return latName;
	}

	/**
	 * @return the page
	 */
	public String getPage() {
		return page;
	}
	
	
}
